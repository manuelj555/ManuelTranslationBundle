<template>
    <div class="row trans-item-header">
        <div class="col-sm-6">
            <h3 class="panel-title" v-if="!isNew">
                {{ code | defaultMessage }}
                <slot name="message"></slot>
            </h3>
            
            <div v-else>
                <input  type="text" class="form-control input-sm" v-model="code" minlength="3">
                <p v-show="errors.required" class="text-danger">{{ $t('error.translation.code.required') }}</p>
                <p v-show="errors.minlength" class="text-danger">{{ $t('error.translation.code.minlength') }}</p>
            </div>
        </div>
        <div class="col-sm-6 text-right">
            <div class="row">
                <div class="col-sm-5 text-left">
                    <small class="text-muted" v-if="!isNew">{{ domain }}</small>
                    <domain-form v-else :domain.sync="domain"></domain-form>
                </div>
                <div class="col-sm-4"><b>{{ $t('label.autogenerated') }}:</b> {{ autogenerated | boolean }}</div>
                <div class="col-sm-3"><b>{{ $t('label.active') }}:</b> {{ active | boolean }}</div>
            </div>
        </div>
    </div>
</template>

<script>
import DomainForm from './DomainForm.vue'
import TransValidation from '../../js/TransValidation'

export default {
    props: {
        code: {required: true, twoWay: true},
        domain: {required: true, twoWay: true},
        autogenerated: {required: true},
        active: {required: true},
        isNew: {required: true, type: Boolean},
        validator: {required: true, type: TransValidation},
    },

    computed: {
        errors () {
            return this.validator.getErrors('code')
        }
    },

    methods: {
    },

    components: {DomainForm},
}
</script>

